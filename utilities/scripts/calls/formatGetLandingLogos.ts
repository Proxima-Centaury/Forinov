/* Format Get Landing Logos */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const formatGetLandingLogos = (response: any): object => {
    return Object.values(response.LOGOS).map((logo: any) => ({
        id: parseInt(logo?.id) || null,
        logo: logo?.logo || null,
        name: logo?.name || null,
    }));
};

/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Get Route Id */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const getRouteId = (routeParameter: string | string[]): number => {
    const id = routeParameter?.toString().substring(routeParameter.indexOf("_") + 1, routeParameter.length);
    return parseInt(id);
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export { getRouteId };
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Scroll To */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const scrollTo = (x: number, y: number): number => {
    const container = document.querySelector("#__next");
    (container) ? container.scrollTo(x as number, y as number - 16) : null;
    return (container?.scrollTop) ? container?.scrollTop : 0;
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export { scrollTo };
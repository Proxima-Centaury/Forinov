const buildFonts = (...fonts: Array<string>): string => {
    const parameters: Array<string> = [];
    const sizes = [
        "0,200", "0,300", "0,400", "0,600", "0,700", "0,800", "0,900",
        "1,200", "1,300", "1,400", "1,600", "1,700", "1,800", "1,900"
    ];
    fonts.map((font: string) => parameters.push("family=" + font.replaceAll(" ", "+") + ":ital,wght@" + sizes.join(";")));
    return parameters.join("&");
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export { buildFonts };
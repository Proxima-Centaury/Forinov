/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Imports */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
import { MouseEventHandler } from "react"
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Base Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface BaseInterface {
    locales: Array<String>,
    states: {
        locale: String
    },
    stateSetters: {
        setLocale: Function,
        setLocales: Function
    }
};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Home Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface HomeInterface extends BaseInterface {};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Navbar Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface NavbarInterface extends BaseInterface {};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Jumbotron Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface JumbotronInterface extends BaseInterface {};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Select Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface SelectInterface {
    type: String,
    version: Number,
    options: Array<Object>,
    action: Function,
    defaultValue: String,
    source: String
};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Button Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface ButtonInterface {
    type?: string,
    faIcon?: Boolean,
    faIconClass?: string,
    url?: String,
    action?: MouseEventHandler<HTMLButtonElement>,
    text?: String,
    count?: Number
};
const ButtonInterfaceDefaults: ButtonInterface = {
    type: "default",
    faIcon: false,
    faIconClass: "",
    url: "",
    action: () => false,
    text: "Button",
    count: 0
};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Option Interface */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
interface OptionInterface extends SelectInterface {
    value: String|Number,
    text: String,
    selected: Boolean
};
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Exports */
/* --------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
export type {
    HomeInterface,
    NavbarInterface,
    JumbotronInterface,
    SelectInterface,
    ButtonInterfaceDefaults,
    OptionInterface
};
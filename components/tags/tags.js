const Tags = ({ tags, main = false, lock = false }) => {
    return <div className={ "tags" + ((lock) ? " locked" : "" )}>
        { tags.map((entry, key) => (key < 3) ? <div key={ key } className={ "tag" + ((main) ? " main" : "") }>
            <p>{ entry[1] }</p>
        </div> : null) }
        { (tags.length > 3) ? <div className="tag">
            <p>{ "+" + (tags.length - 3) }</p>
        </div> : null}
    </div>;
};
/* ------------------------------------------------------------------------------------------------------------------------------------------- */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------- */
export default Tags;
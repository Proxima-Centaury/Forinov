/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Types */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
import type { TagType } from "@typescript/types/TagType";
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Styles */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
import TagStyles from "@tags/Tag.module.css";
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Primary Tag */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const PrimaryTag = (params: TagType): JSX.Element => {
    const { color, tag } = params;
    return <p className={ TagStyles.primary + ((color) ? ` ${ TagStyles[color] }` : "") }>{ tag }</p>;
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export default PrimaryTag;
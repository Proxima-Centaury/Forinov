const Grid = (params: any): JSX.Element => {
    const { t } = useTranslation("common");
    const { columns, cards, type } = params;
    return <div className={ `grid ${ (columns) ? columns : "two" }Columns` }>
        { (type && type ===  "deals") ? cards?.map((deal: any, key: number) => {
            return <Fragment key={ key }>
                <OpportunityCard { ...deal }/>
            </Fragment>;
        }) : null }
    </div>;
};

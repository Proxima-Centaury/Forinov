/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Types */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
import type { TTooltip } from "@typescript/types/TTooltip";
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Tooltip */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const Tooltip = (params: TTooltip): JSX.Element => {
    const { children, tooltip } = params;
    return <div data-type="tooltip" data-tooltip={ tooltip }>
        { children }
    </div>;
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export default Tooltip;
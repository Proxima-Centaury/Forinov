/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Imports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
import { GetStaticProps } from "next";
import { Fragment, useEffect } from "react";
import { LoginInterface } from "../../typescript/interfaces";
import { redirectTo } from "../../scripts/utilities";
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Components */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
import MetaDatas from "../../components/seo/metadatas/metadatas";
import LoginCard from "../../components/cards/login";
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Login */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const Login = (pageProps: LoginInterface) => {
    const { states, router } = pageProps;
    const { locale, session } = states;
    useEffect(() => { (session) ? redirectTo("/", router, locale) : null });
    return <Fragment>
        <MetaDatas { ...pageProps }/>
        <div id="login" className="container">
            <LoginCard { ...pageProps } administration/>
        </div>
    </Fragment>;
};
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Static Props */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
const getStaticProps: GetStaticProps = async (context) => ({ props: { ...context } });
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
/* Exports */
/* ------------------------------------------------------------------------------------------------------------------------------------------------ */
export default Login;
export { getStaticProps };